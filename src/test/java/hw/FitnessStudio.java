package hw;

public class FitnessStudio {
    private String individualTraining;
    private String groupTraining;
    private String gym;
    private String yogaIndividual;
    private String yogaGroup;
    private String tabata;
    private int price;
    private boolean attendance;

    public String getIndividualTraining() {
        return individualTraining;
    }

    public void setIndividualTraining(String individualTraining) {
        this.individualTraining = individualTraining;
    }

    public String getGroupTraining() {
        return groupTraining;
    }

    public void setGroupTraining(String groupTraining) {
        this.groupTraining = groupTraining;
    }

    public String getGym() {
        return gym;
    }

    public void setGym(String gym) {
        this.gym = gym;
    }

    public String getYogaIndividual() {
        return yogaIndividual;
    }

    public void setYogaIndividual(String yogaIndividual) {
        this.yogaIndividual = yogaIndividual;
    }

    public String getYogaGroup() {
        return yogaGroup;
    }

    public void setYogaGroup(String yogaGroup) {
        this.yogaGroup = yogaGroup;
    }

    public String getTabata() {
        return tabata;
    }

    public void setTabata(String tabata) {
        this.tabata = tabata;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public boolean isAttendance() {
        return attendance;
    }

    public void setAttendance(boolean attendance) {
        this.attendance = attendance;
    }
}
