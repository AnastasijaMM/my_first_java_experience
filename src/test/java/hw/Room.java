package hw;

public class Room {
    private int chair;
    private int table;
    private int tv;
    private int lamp;
    private int cupboard;

    public int getChair() {
        return chair;
    }

    public void setChair(int chair) {
        this.chair = chair;
    }

    public int getTable() {
        return table;
    }

    public void setTable(int table) {
        this.table = table;
    }

    public int getTv() {
        return tv;
    }

    public void setTv(int tv) {
        this.tv = tv;
    }

    public int getLamp() {
        return lamp;
    }

    public void setLamp(int lamp) {
        this.lamp = lamp;
    }

    public int getCupboard() {
        return cupboard;
    }

    public void setCupboard(int cupboard) {
        this.cupboard = cupboard;
    }
}
