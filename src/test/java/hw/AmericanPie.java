package hw;

public class AmericanPie {
    private int brownSugar;
    private int whiteSugar;
    private int flour;
    private int cinnamon;
    private int egg;
    private int apples;

    public int getBrownSugar() {
        return brownSugar;
    }

    public void setBrownSugar(int brownSugar) {
        this.brownSugar = brownSugar;
    }

    public int getWhiteSugar() {
        return whiteSugar;
    }

    public void setWhiteSugar(int whiteSugar) {
        this.whiteSugar = whiteSugar;
    }

    public int getFlour() {
        return flour;
    }

    public void setFlour(int flour) {
        this.flour = flour;
    }

    public int getCinnamon() {
        return cinnamon;
    }

    public void setCinnamon(int cinnamon) {
        this.cinnamon = cinnamon;
    }

    public int getEgg() {
        return egg;
    }

    public void setEgg(int egg) {
        this.egg = egg;
    }

    public int getApples() {
        return apples;
    }

    public void setApples(int apples) {
        this.apples = apples;
    }
}
